'''Từ khóa def trong Python được sử dụng để định nghĩa hàm . Nó cho phép bạn tạo các khối mã có thể tái sử dụng với một tên cụ thể, làm cho mã của bạn trở thành mô-đun, có tổ chức và dễ bảo trì hơn'''
# Cấu trúc khởi tạo một hàm : 
# def + tên hàm():
#     Tất cả nội dung thụt vào so với với hàm đều là nằm bên trong hàm

def plus(a,b):
    print(a+b)
# Chạy một hàm cú pháp tên hàm + ()
# plus()
# parameter- tham số truyền vào hàm 
plus(a=1,b=2)

def phepnhan(a,b):
    print(a*b)
